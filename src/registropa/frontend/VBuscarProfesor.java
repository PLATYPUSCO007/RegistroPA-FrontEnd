    private void modelotabla() {
        tmodel.addColumn("Id");
        tmodel.addColumn("Nombre");
        tmodel.addColumn("Apellido");
        tmodel.addColumn("Tipo Ident");
        tmodel.addColumn("NÂº Ident");
        jTable1.setModel(tmodel);
    }

    private void datosModeloTabla(){
        try {
            tmodel.setRowCount(0);
            profesoresbuscados = profesorcontroller.buscarPro();
            for (int i = 0; i < profesoresbuscados.size(); i++) {
                profesor = new String[]{String.valueOf(profesoresbuscados.get(i).getId()),
                    profesoresbuscados.get(i).getNombre(),
                    profesoresbuscados.get(i).getApellidos(),
                    profesoresbuscados.get(i).getTipoIdentificacion().name(),
                    String.valueOf(profesoresbuscados.get(i).getIdentificacion()),

                };
                tmodel.addRow(profesor);
            }
        } catch (Exception e) {
        }
    }
    
    
